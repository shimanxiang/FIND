package find.entity;

public class Corporation {
	private int corporation_id;
	private String corporation_name;
	private String corporation_head_link;
	private String corporation_instruction;
	
	public int getCorporation_id() {
		return corporation_id;
	}
	public void setCorporation_id(int corporation_id) {
		this.corporation_id = corporation_id;
	}
	public String getCorporation_name() {
		return corporation_name;
	}
	public void setCorporation_name(String corporation_name) {
		this.corporation_name = corporation_name;
	}
	public String getCorporation_head_link() {
		return corporation_head_link;
	}
	public void setCorporation_head_link(String corporation_head_link) {
		this.corporation_head_link = corporation_head_link;
	}
	public String getCorporation_instruction() {
		return corporation_instruction;
	}
	public void setCorporation_instruction(String corporation_instruction) {
		this.corporation_instruction = corporation_instruction;
	}
	
}
