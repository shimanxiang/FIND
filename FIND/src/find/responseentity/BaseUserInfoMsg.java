package find.responseentity;

public class BaseUserInfoMsg {
	
	private String user_mail;
	private String user_nickname;
	private String user_type;
	private String user_class_name;
	private String user_corporation_name;
	
	public String getUser_mail() {
		return user_mail;
	}
	public void setUser_mail(String user_mail) {
		this.user_mail = user_mail;
	}
	public String getUser_nickname() {
		return user_nickname;
	}
	public void setUser_nickname(String user_nickname) {
		this.user_nickname = user_nickname;
	}
	public String getUser_type() {
		return user_type;
	}
	public void setUser_type(String user_type) {
		this.user_type = user_type;
	}
	public String getUser_class_name() {
		return user_class_name;
	}
	public void setUser_class_name(String user_class_name) {
		this.user_class_name = user_class_name;
	}
	public String getUser_corporation_name() {
		return user_corporation_name;
	}
	public void setUser_corporation_name(String user_corporation_name) {
		this.user_corporation_name = user_corporation_name;
	}
}
